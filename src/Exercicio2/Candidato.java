package Exercicio2;

public class Candidato  {
    private int matricula;
    private double nota;

    public Candidato(int matricula, double nota) {
        this.matricula = matricula;
        this.nota = nota;
    }

    public int getMatricula() {
        return matricula;
    }

    public double getNota() {
        return nota;
    }
}
